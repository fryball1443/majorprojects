public class Age
{
   public static void main(String[] args)
   {
      int age = 18;
      int remainingYears = 100 - age;
      int halfAge = age / 2;
      System.out.println("My age is: " + age + "\n" + "Years till I am 100: " + remainingYears + 
                         "\n" + "Half my age is: " + halfAge);
      
   }
}