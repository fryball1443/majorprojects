#!/bin/bash

# Define colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
CYAN='\033[0;36m'
WHITE='\033[0;37m'

# Define ASCII art
ART="
${RED}   _____ _                 _       _     
${GREEN}  / ____(_)               | |     | |    
${YELLOW} | (___  _ _ __ ___  _ __ | | __ _| |__  
${BLUE}  \___ \| | '_ \` _ \| '_ \| |/ _\` | '_ \ 
${PURPLE}  ____) | | | | | | | |_) | | (_| | |_) |
${CYAN} |_____/|_|_| |_| |_| .__/|_|\__,_|_.__/ 
${WHITE}                    | |                  
${RED}                    |_|                  
"

# Print ASCII art
echo -e "$ART"

